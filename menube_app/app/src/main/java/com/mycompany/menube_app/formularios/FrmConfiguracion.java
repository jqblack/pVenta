    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        // TODO add your handling code here:

        try {
            String sql = "select  * from t_configuracion where f_pass = '" + pass.getText() + "'";
            ResultSet rs = dao.getQuery(sql);

            if (rs.next()) {
                empresa.setEnabled(true);
                printer.setEnabled(true);
                url.setEnabled(true);
                salvar.setEnabled(true);
                rdTipoEpson.setEnabled(true);
                rdTipoStart.setEnabled(true);
                rdTipoMatricial.setEnabled(true);
                rdTipoTermico.setEnabled(true);
                url_rnc.setEnabled(true);

                dao.closeStatement();
            } else {
                funciones.mensaje("La Contraseña es incorrecta ..");
                return;
            }

        } catch (Exception e) {
            funciones.mensaje(e.getMessage());
        }

    }//GEN-LAST:event_okActionPerformed

