    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed

        if (url.getText().equals("")) {
            funciones.mensaje("Digite el url");
            return;

        }
        if (empresa.getText().equals("")) {
            funciones.mensaje("Digite la empresa por defecto");
            return;
        }

        try {

            int tipo_p = 1;//start
            if (rdTipoEpson.isSelected()) {
                tipo_p = 2;//printer epson
            }

            int tipo_papel_printer = 1;//Printer matricial
            if (rdTipoTermico.isSelected()) {
                tipo_papel_printer = 2;//printer Termico
            }

            dao.actualizar_configuracion(
                    empresa.getText(),
                    ((Combo) printer.getSelectedItem()).getId(),
                    printer.getSelectedItem().toString(),
                    url.getText(),
                    tipo_p + "",
                    tipo_papel_printer + "",url_rnc.getText()
            );
            funciones.mensaje("Datos Registrados Correctamente");
            dispose();

        } catch (Exception e) {
            funciones.mensaje(e.getMessage());
        }


    }//GEN-LAST:event_salvarActionPerformed

