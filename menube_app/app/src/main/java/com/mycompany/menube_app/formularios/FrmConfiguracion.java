    private void llenar() {
        PrintService[] ps = PrintServiceLookup.lookupPrintServices(null, null);
        for (int i = 0; i < ps.length; i++) {
            Combo datos = new Combo(i + "", ps[i].getName());
            printer.addItem(datos);
        }
        empresa.setText(dao.getEmpresa());
        url_rnc.setText(dao.getUrl_rnc());
        funciones.setValorCombo(printer, dao.getIdprinter());
        url.setText(dao.getUrl());
        if (dao.getTipo_printer().equals("1")) {
            rdTipoStart.setSelected(true);
        } else {
            rdTipoEpson.setSelected(true);
        }

        if (dao.getTipo_papel().equals("1")) {
            rdTipoMatricial.setSelected(true);
        } else {
            rdTipoTermico.setSelected(true);
        }

    }

