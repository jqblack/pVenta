    public void llenar_datos_clientes(String idrecord) {
        Map map;
        List<Map<String, Object>> map1;
        String respuesta = "";
        try {
            respuesta = webServices.buscar("\"f_idrecord\":\"" + idrecord + "\"", "ws/get_clientes_by_id");
            if (!respuesta.equals("")) {
                map = funciones.JsonToMap(respuesta);
                map1 = funciones.JsonToListMap(map.get("f_data"));
                for (Map tupla : map1) {
                    if (!Boolean.valueOf(tupla.get("f_tiene_credito").toString())) {
                        funciones.mensaje("El cliente no posee credito con nosotros", "", JOptionPane.WARNING_MESSAGE);
                        return;
                    }
                    setIdcliente(Integer.valueOf(idrecord));
                    String nombre_cliente = "";
                    String direccion = "";
                    String limite_credito = "";
                    String codigo = "";
                    nombre_cliente += tupla.get("f_nombre").toString() != null ? tupla.get("f_nombre").toString() : "";
                    nombre_cliente += tupla.get("f_apellido").toString() != null ? tupla.get("f_apellido").toString() : "";
                    direccion = tupla.get("f_direccion").toString() != null ? tupla.get("f_direccion").toString() : "";
                    limite_credito = tupla.get("f_limite_credito").toString() != null ? tupla.get("f_limite_credito").toString() : "";
                    codigo = tupla.get("f_codigo_cliente").toString() != null ? tupla.get("f_codigo_cliente").toString() : "";

                    txtnombreCliente.setEditable(true);
                    txtcodigocliente.setEditable(true);
                    txtDirecicon.setEditable(true);
                    txtCreditoDisponible.setEditable(true);

                    txtnombreCliente.setText(nombre_cliente);
                    txtcodigocliente.setText(codigo);
                    txtDirecicon.setText(direccion);
                    txtCreditoDisponible.setText(funciones.Round(limite_credito, 2));

                    txtnombreCliente.setEditable(false);
                    txtcodigocliente.setEditable(false);
                    txtDirecicon.setEditable(false);
                    txtCreditoDisponible.setEditable(false);

                }
            }

        } catch (Exception ex) {
            System.err.println("FrmCerrarFactura => cargar datos cliente => " + ex.getMessage());
        }
    }
    
    public void eventoValidarNcf()
    {
        Map map;
        String respuesta = "";
        try {
            respuesta = webServices.buscar("\"f_idrecord\":\"" + ((Combo)cbNcf.getSelectedItem()).getId() + "\"", "ws/get_validar_ncf");
            if (!respuesta.equals("")) {
                map = funciones.JsonToMap(respuesta);
                String [] split = map.get("f_data").toString().split("#");
                
                
                if (split[0].equals("-1")) {
                    funciones.mensaje("El comprobante seleccionado a caducado su vigencia, por favor llame a la administracion","",JOptionPane.ERROR_MESSAGE);
                    btnfacturar.setEnabled(false);
                    _valida_rnc = false;
                }else{
                    if (split[1].equals("-1")) {
                        _valida_rnc = true;
                        FrmValidarRnc frm = new FrmValidarRnc((JFrame)this.getParent(),true);
                        frm.setVisible(true);
                        frm.addWindowListener(new WindowListener() {
                            @Override
                            public void windowOpened(WindowEvent e) {
                            }

                            @Override
                            public void windowClosing(WindowEvent e) {
                            }

                            @Override
                            public void windowClosed(WindowEvent e) {
                                if (frm.getStatus().equals("ACTIVO")) {
                                    _valido_rnc = true;
                                    setRnc(frm.getRnc());
                                    setRazon_social(frm.getNombre());                                    
                                  
                                }else{
                                    _valido_rnc = false;
                                     setRnc("");
                                    setRazon_social("");
                                }
                                
                            }

                            @Override
                            public void windowIconified(WindowEvent e) {
                            }

                            @Override
                            public void windowDeiconified(WindowEvent e) {
                            }

                            @Override
                            public void windowActivated(WindowEvent e) {
                            }

                            @Override
                            public void windowDeactivated(WindowEvent e) {
                            }
                        });
                    }else{
                        _valida_rnc = false;
                    }
                     btnfacturar.setEnabled(true);
                }
            }

        } catch (Exception ex) {
            System.err.println("FrmCerrarFactura => validar rnc => " + ex.getMessage());
        }
    }

    public void eventoFacturar() {

        Double monto_cambio = 0.00;
        Double monto_efectivo = 0.00;
        Double monto_tarjeta = 0.00;
        Double monto_promina = 0.00;

        if (((Combo) cbNcf.getSelectedItem()).getId().equals("0")) {
            funciones.mensaje("Seleccione el NCF", "", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        setTipo_ncf(Integer.valueOf(((Combo) cbNcf.getSelectedItem()).getId()));
        if (_valida_rnc==true) {
            if (_valido_rnc==false) {
                funciones.mensaje("Por Favor valide un RNC,se eligio el comprobante "+((Combo)cbNcf.getSelectedItem()).getNombre());
                return;
            }
        }
        
        if (getTipo_pago() == 0 || getTipo_pago() == 1) {

            if (!txtefectivo.getText().equals("")) {
                monto_cambio = funciones.parseDouble(txtcambio.getText());
                monto_efectivo = funciones.parseDouble(txtefectivo.getText());
            }

            if (!txtPropina.getText().equals("")) {
                monto_promina = funciones.parseDouble(txtPropina.getText());
            }

            if (!txtmontoTarjeta.getText().equals("")) {
                monto_tarjeta = funciones.parseDouble(txtmontoTarjeta.getText());
            }

            if (monto_tarjeta > 0) {

                if (txtautorizacionTarjeta.getText().equals("")) {
                    funciones.mensaje("Digite la autorizacion ", "", JOptionPane.INFORMATION_MESSAGE);
                    return;
                } else {
                    setNumero_autorizacion(txtautorizacionTarjeta.getText());
                }

                if (((Combo) cbtipoTarjeta.getSelectedItem()).getId().equals("0")) {

                    funciones.mensaje("Seleccione el tipo de tarjeta ", "", JOptionPane.INFORMATION_MESSAGE);
                    return;
                } else {
                    setTipo_tarjeta(Integer.valueOf(((Combo) cbtipoTarjeta.getSelectedItem()).getId()));
                }
            }

            Double monto_cobrar = funciones.parseDouble(txtmontoPagar.getText());
            Double total = monto_efectivo + monto_tarjeta;

            if (monto_cobrar > total) {
                funciones.mensaje("El monto efectivo o tarjeta no es suficiente para cerrar la factura ", "", JOptionPane.WARNING_MESSAGE);
                return;
            }

            setMonto_por_tarjeta(monto_tarjeta);
            setPropina_dada(monto_promina);
            setDevuelta(monto_cambio);
            setMonto_efetivo(monto_efectivo);
            
            
            
            dispose();

        }


        if (getTipo_pago() == 2) {
            if (getIdcliente() == 0 || (getIdcliente() + "").equals("")) {
                funciones.mensaje("Seleccione el cliente", "", JOptionPane.WARNING_MESSAGE);
                return;
            } else {
                Double monto_pagar = funciones.parseDouble(txtmontoPagar.getText());
                Double credito_disponible = funciones.parseDouble(txtCreditoDisponible.getText());

                if (monto_pagar > credito_disponible) {
                    funciones.mensaje("EL monto de la factura es mayor que el credito del cliente", "", JOptionPane.WARNING_MESSAGE);
                    return;
                } else {
                    dispose();
                }
            }
        }
    }

