    public String CrearJsonDetalles() {
        String detalle = "";
        String aux_detalle = "";

        for (int i = 0; i < tabla.getRowCount(); i++) {
            DefaultTableModel modelo = (DefaultTableModel)tabla.getModel();
            Double valor = funciones.parseDouble(modelo.getValueAt(i, 3).toString());
            Double cantidad_en_tabla =0.00;
            
            if(!modelo.getValueAt(i, 2).toString().equals("")){
                cantidad_en_tabla = funciones.parseDouble(modelo.getValueAt(i, 2).toString());
            }
            String billete= modelo.getValueAt(i, 1).toString();
            String idrecord = modelo.getValueAt(i, 0).toString();

            aux_detalle = "{"
                    + "'f_idrecord':'" + idrecord + "',"
                    + "'f_billete':'" + billete + "',"
                    + "'f_cantidad':'" + cantidad_en_tabla + "',"
                    + "'f_valor':'" + valor + "'"
                    + "},";

            detalle += aux_detalle;
        }

        detalle = "[" + detalle + "]";
        detalle = detalle.replace(",]", "]");

        return detalle;
    }
    
