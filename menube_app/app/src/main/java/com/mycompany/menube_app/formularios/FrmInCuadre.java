    public void llenar_billetes() {

        funciones.eliminar_tabla(tabla);

        Map map;
        List<Map<String, Object>> map1;
        String respuesta = "";
        try {
            respuesta = webServices.buscar("", "get_lista_tipo_billete");
            if (!respuesta.equals("")) {

                map = funciones.JsonToMap(respuesta);
                map1 = funciones.JsonToListMap(map.get("f_data"));

                for (Map tupla : map1) {

                    DefaultTableModel model = (DefaultTableModel) tabla.getModel();
                    model.addRow(new Object[]{
                        tupla.get("f_idrecord").toString(),
                        tupla.get("f_descripcion").toString(),
                        "",
                        tupla.get("f_valor").toString(),});
                }

            }

        } catch (Exception ex) {
            System.err.println(ex.getMessage());
        }
    }

    public void calcular() {
        Double total = 0.00;
        for (int i = 0; i < tabla.getRowCount(); i++) {

            DefaultTableModel model = (DefaultTableModel) tabla.getModel();
            Double cantidad = funciones.parseDouble(model.getValueAt(i, 2).toString());
            Double valor = funciones.parseDouble(model.getValueAt(i, 3).toString());

            total += cantidad * valor;
        }
        txttotalBilletes.setText(funciones.Round(total.toString(), 2));

    }
